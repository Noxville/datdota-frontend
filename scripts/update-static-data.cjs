async function main() {
  console.log('Updating static data from api.datdota.com...\n');

  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  }

  const tasks = [
    updateHeroes,
    updatePatches,
    updateAbilities,
    updateItems,
    updateFrames,
    updateUnits,
    updateBuildings,
  ];

  const failed = [];
  const succeeded = [];

  for (const task of tasks) {
    try {
      await task();
      succeeded.push(task.name);
    } catch (error) {
      console.error(`  FAILED: ${error.message}`);
      failed.push(task.name);
    }
  }

  // Create index file — only export modules that succeeded
  const moduleMap = {
    updateHeroes: 'heroes',
    updatePatches: 'patches',
    updateAbilities: 'abilities',
    updateItems: 'items',
    updateFrames: 'frames',
    updateUnits: 'units',
    updateBuildings: 'buildings',
  };

  const exports = succeeded
    .map(name => moduleMap[name])
    .filter(Boolean)
    .map(mod => `export * from './${mod}'`)
    .join('\n');

  const indexOutput = `// Auto-generated by scripts/update-static-data.cjs\n${exports}\n`;
  fs.writeFileSync(path.join(OUTPUT_DIR, 'index.ts'), indexOutput);

  if (failed.length > 0) {
    console.log(`\nDone with ${failed.length} failure(s): ${failed.join(', ')}`);
    console.log('Successfully updated: ' + succeeded.map(n => moduleMap[n]).join(', '));
  } else {
    console.log('\nDone! All static data updated successfully.');
  }
}

main();
