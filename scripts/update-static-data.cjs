#!/usr/bin/env node

/**
 * Fetches static data from api.datdota.com and generates
 * TypeScript files with typed lookup maps.
 *
 * Usage: node scripts/update-static-data.cjs
 */

const fs = require('fs');
const path = require('path');

const API_BASE = 'https://api.datdota.com';
const OUTPUT_DIR = path.join(__dirname, '../src/data');

async function fetchJson(url) {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status}`);
  }
  return response.json();
}

function sortByNumericKey(obj) {
  const sorted = {};
  const keys = Object.keys(obj).sort((a, b) => parseInt(a) - parseInt(b));
  for (const key of keys) {
    sorted[key] = obj[key];
  }
  return sorted;
}

async function updateHeroes() {
  console.log('Fetching heroes...');
  const { data } = await fetchJson(`${API_BASE}/api/static/heroes`);
  // data is keyed by hero ID: { "1": { name, picture }, ... }
  const heroCount = Object.keys(data).length;
  console.log(`  Found ${heroCount} heroes`);

  const output = `// Auto-generated by scripts/update-static-data.cjs
// Last updated: ${new Date().toISOString()}

export interface HeroData {
  name: string
  picture: string
}

/** Map of hero ID to hero data */
export const heroesById: Record<string, HeroData> = ${JSON.stringify(sortByNumericKey(data), null, 2)}

/** Get hero by numeric ID */
export function getHeroById(id: number | string): HeroData | undefined {
  return heroesById[String(id)]
}

/** Total number of heroes */
export const HERO_COUNT = ${heroCount}
`;

  fs.writeFileSync(path.join(OUTPUT_DIR, 'heroes.ts'), output);
  console.log('  Written to src/data/heroes.ts');
}

async function updatePatches() {
  console.log('Fetching patches...');
  const { data } = await fetchJson(`${API_BASE}/api/static/patches`);
  // data is an array: [{ name, id }, ...]
  console.log(`  Found ${data.length} patches`);

  const output = `// Auto-generated by scripts/update-static-data.cjs
// Last updated: ${new Date().toISOString()}

export interface PatchData {
  name: string
  lower_bound: number
  upper_bound: number
}

/** All patches, newest first */
export const patches: PatchData[] = ${JSON.stringify(data, null, 2)}

/** All patch names, newest first */
export const patchNames: string[] = patches.map(p => p.name)

/** Get patch by name */
export function getPatchByName(name: string): PatchData | undefined {
  return patches.find(p => p.name === name)
}

/** Total number of patches */
export const PATCH_COUNT = ${data.length}
`;

  fs.writeFileSync(path.join(OUTPUT_DIR, 'patches.ts'), output);
  console.log('  Written to src/data/patches.ts');
}

